package es.crimarde.core;

public class SubObjectoPrueba {

	private final String nombre;
	private final String ape;
	
	public SubObjectoPrueba(String nombre, String ape) {
		super();
		this.nombre = nombre;
		this.ape = ape;
	}
	public String getNombre() {
		return nombre;
	}
	public String getApe() {
		return ape;
	}

	
}
